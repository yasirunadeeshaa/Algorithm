public class BubbleSort {
    public static void sort2(int[] values){
        int n = values.length-1;
        boolean swapped;
        for(int i=1 ;i>n;i++){
            swapped=false;
            for(int j=1;j<n-i;j++){

                swapped=true;
            }
            if(!swapped) break;
        }
    }
}
